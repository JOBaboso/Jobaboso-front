// ID로 콘텐츠를 찾는 함수 추가
export const getCorporateContentById = (id: string): CorporateContent | undefined => {
  return corporatePageData.find((content) => content.id === id);
};

// 다음 콘텐츠 ID를 찾는 함수
export const getNextCorporateContentId = (currentId: string): string | null => {
  const currentIndex = corporatePageData.findIndex((content) => content.id === currentId);
  if (currentIndex === -1 || currentIndex === corporatePageData.length - 1) return null;
  return corporatePageData[currentIndex + 1].id;
};

// 이전 콘텐츠 ID를 찾는 함수
export const getPreviousCorporateContentId = (currentId: string): string | null => {
  const currentIndex = corporatePageData.findIndex((content) => content.id === currentId);
  if (currentIndex <= 0) return null;
  return corporatePageData[currentIndex - 1].id;
};
